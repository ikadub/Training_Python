def test_function():
    # Объявляем внутреннюю функцию
    def inner_function():
        print("Я в области видимости функции test_function")

    # Вызываем внутреннюю функцию внутри внешней
    inner_function()
    # ничего не печатает, так как все действия происходят внутри внешней функции


# Вызываем внешнюю функцию
test_function() # Видим надпись "Я в области..."

# Попробуем вызвать внутреннюю функцию снаружи
try: # используем исключения
    inner_function()  # Это вызовет ошибку NameError, так как inner_function недоступна за пределами test_function
except Exception as e: # сохраняем объект в переменную e
    print(f"Произошла ошибка при попытке вызова внутренней функции: {e}")